<template>
  <form @submit.prevent="onSubmit">
    <input type="text" name="title" v-model="title" placeholder="Add todo ...">
    <input type="submit" value="Submit" class="btn" />
  </form>
</template>

<script>
export default {
  data() {
    return {
      title: ''
    }
  },
  methods: {
    onSubmit() {
      if (this.title.trim()) {
        const newTodo = {
          id: Date.now(),
          title: this.title,
          completed: false
        }

        this.$emit('add-todo', newTodo)
        this.title = ''
      }
    }
  }
}
</script>

<style scoped>
  form {
    display: flex;
  }

  input[type="text"] {
    flex: 10;
    padding: 5px;
  }

  input[type="submit"]{
    flex: 2;
  }
</style>